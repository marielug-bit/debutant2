# What is a module?
# A module is a Python file that contains useful code
# that we can import and reuse in another file.

# List three ways to import a module in Python.
# 1) import module_name
# 2) import module_name as m
# 3) from module_name import func

# What is the purpose of importing?
# Importing allows us to reuse code that is useful in many files,
# so we do not need to rewrite it each time.

# List three examples when you would use the random module.
# 1) Generate a random number between 1 and 6 to simulate a die.
# 2) Randomly pick a value from a list.
# 3) Shuffle a list (like a deck of cards).

# What is an ImportError?
# An ImportError occurs when Python cannot find or load
# the module we are trying to import.

# When would using an OrderedDict be useful?
# When you need to preserve the order in which key-value pairs
# were added to a dictionary.

# When would using a defaultdict be useful?
# When you want a dictionary that automatically provides
# a default value for missing keys instead of raising a KeyError.

